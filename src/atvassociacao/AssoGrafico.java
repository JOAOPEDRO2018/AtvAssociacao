    private void botaoAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoAddActionPerformed
    
    if(id == 0){
        JOptionPane.showMessageDialog(null,"Selecione um ID válido","Atenção",JOptionPane.ERROR_MESSAGE);
    }
    
    if(BoxProduto.getSelectedItem() == "selecione>>"){
        JOptionPane.showMessageDialog(null,"Selecione uma opção!","Erro",
                JOptionPane.ERROR_MESSAGE);
    }
    else{
        
        produtos.add(BoxProduto.getSelectedItem().toString());
        precoProdutos.add(Textpreco.getText());
        valor = Integer.parseInt(Textpreco.getText());
        i += valor;
        butaoFinalizar.setVisible(true); 
    }  
    
    }//GEN-LAST:event_botaoAddActionPerformed

    private void boxVendedorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_boxVendedorActionPerformed
    id = boxVendedor.getSelectedIndex();
     
    if(id == 1){
        TextVendedor.setText("João Pedro");
     }
    if(id == 2){
        TextVendedor.setText("Kelly"); 
    }
    if(id == 0){
        JOptionPane.showMessageDialog(null,"Selecione um ID válido","Atenção",JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_boxVendedorActionPerformed

    private void BoxProdutoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BoxProdutoActionPerformed
        int item = BoxProduto.getSelectedIndex();
       
        if(item == 0){
        JOptionPane.showMessageDialog(null,"Selecione um produto válido","Atenção",JOptionPane.ERROR_MESSAGE);
        }
        
        if(item == 1){
            Textpreco.setText("40"); 
        }
        if(item == 2){
            Textpreco.setText("45"); 
        }
        if(item == 3){
            Textpreco.setText("35");
        }
        if(item == 4){
            Textpreco.setText("60"); 
        }
        if(item == 5){
            Textpreco.setText("1060"); 
        }
    }//GEN-LAST:event_BoxProdutoActionPerformed

