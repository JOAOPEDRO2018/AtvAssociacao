    private void butaoFinalizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_butaoFinalizarActionPerformed
    
        if(!"".equals(TextCliente.getText())
                && !"".equals(TextEndereco.getText())
                && !"".equals(TextNcasa.getText())){
            comissao = i*0.2;
            
            JOptionPane.showMessageDialog(this,"Vendedor: "+TextVendedor.getText()
                  +"\nCliente: "+TextCliente.getText()+"\nProduto(s): "
                  +new ArrayList(produtos)+"\nValor total: R$"+ i
                  +"\nComissão do vendedor: R$"+comissao);
        }
        else{
           JOptionPane.showMessageDialog(this,"Digite todos os dados do cliente!",
                   "Atenção",JOptionPane.INFORMATION_MESSAGE);
        }
        
        
    }//GEN-LAST:event_butaoFinalizarActionPerformed

